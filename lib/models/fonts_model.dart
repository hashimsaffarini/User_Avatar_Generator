/// Enum representing the various font styles available for avatars.
///
/// This enum allows you to choose different fonts for displaying text
/// in user avatars, offering a variety of styles to suit different themes.
enum AvatarFontStyles {
  /// A bold and impactful font style suitable for strong statements.
  protestGuerrilla,

  /// A playful and rounded font style with a casual feel.
  pacifico,

  /// A clean and modern sans-serif font style.
  anton,

  /// A stylish and elegant font with a unique design.
  concertOne,

  /// A fun and energetic font style with a comic-like appearance.
  bangers,

  /// A versatile and readable font style with a contemporary look.
  chivo,
}
